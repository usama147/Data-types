z = 5  # Declaring z as a variable 5.
y = 5.8  # Declaring y as a variable 5.8.
print(type(z))  # Prints data type of variable z.
print(type(y))  # Prints data type of variable y.
mylist = [23, 82, 42, "Andrew"]  # Declaring a list.
print(type(mylist))  # Prints data type of "mylist".
